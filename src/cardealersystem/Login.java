    private void LoginIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginIActionPerformed
      if (password.getText().isEmpty() || username.getText().isEmpty()) {
          JOptionPane.showMessageDialog(null, "All Fields are Required");
            Login lf = new Login();
            lf.setVisible(true);
            this.dispose();            
        }else if (loginAcc(username.getText(), password.getText())) {

            if (!status.equals("Active")) {
                JOptionPane.showMessageDialog(null, "Inactive Account. Contact Admin at 09289873493");
            }else {
                session ses = session.getInstance(); 
                String logMessage = "User Login: " + ses.getUn();
                String timestamp = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new java.util.Date());

                try {
                    dbConnector dbc = new dbConnector();
                    String logSql = "INSERT INTO tbl_logs (u_id, actions, date) VALUES (?, ?, ?)";
                    PreparedStatement logStmt = dbc.getConnection().prepareStatement(logSql);
                    logStmt.setInt(1, ses.getId()); 
                    logStmt.setString(2, logMessage);
                    logStmt.setString(3, timestamp);
                    logStmt.executeUpdate();
                    logStmt.close();
                }catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Failed to write login log.");
                }

                JOptionPane.showMessageDialog(null, "Login Success!");
                if (type.equals("Admin")) {
                    Dashboard ads = new Dashboard();
                    ads.setVisible(true);
                    this.dispose();
                } else if (type.equals("Dealer")) {
                    Dealer_Dashboard ddb = new Dealer_Dashboard();
                    ddb.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "No Account Type Found, Contact Admin");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Login Failed");
            
      }
    }//GEN-LAST:event_LoginIActionPerformed
  
