   public static boolean loginAcc(String username, String password){
    dbConnector connector = new dbConnector();
 
    try {
        String query = "SELECT * FROM tbl_users WHERE u_username = '" + username + "'";
        ResultSet resultSet = connector.getData(query);
        if(resultSet.next()){
                       
                String hashedPass = resultSet.getString("u_password");
                String rehashedPass = pasHasher.hashPassword(password);
                
                if(hashedPass.equals(rehashedPass)){
                     status = resultSet.getString("u_status");
                     type = resultSet.getString("u_type");
                     session ses = session.getInstance();
                     ses.setId(resultSet.getInt("u_id"));
                     ses.setFn(resultSet.getString("u_fname"));
                     ses.setLn(resultSet.getString("u_lname"));
                     ses.setEm(resultSet.getString("u_email"));
                     ses.setCt(resultSet.getString("u_contact"));
                     ses.setUn(resultSet.getString("u_username"));
                     ses.setTacc(resultSet.getString("u_type"));
                     ses.setStat(resultSet.getString("u_status"));
                    
                     return true;                              
                }else{
                   return false;
               }                        
        }else{
            System.out.println("Password isnt Match!");
            return false;
        }
    } catch (SQLException |NoSuchAlgorithmException ex) {
        System.out.println(""+ex);
        return false;
    }
}

            

